    b_time = round_letro(p * scale)

    # ï¼»ç™½å‹ã¡ã ã‘ã§ã®å¯¾å±€æ•°ï¼½åŸºç¤
    w_time = scale - b_time

    # ç´„åˆ†ã™ã‚‹
    fraction = Fraction(b_time, w_time)
    return fraction.numerator, fraction.denominator


