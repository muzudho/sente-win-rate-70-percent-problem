    # èª¬æ˜ï¼’  ã‚³ã‚¤ãƒ³ã®è¡¨è£ã®ç¢ºç‡ã®æ•´æ•°åŒ–
    # --------------------------------
    scale = scale_for_float_to_int(p)

    # ï¼»é»’å‹ã¡ã ã‘ã§ã®å¯¾å±€æ•°ï¼½åŸºç¤
    #
    #   NOTE int() ã‚’ä½¿ã£ã¦å°æ•°ç‚¹ä»¥ä¸‹åˆ‡ã‚Šæ¨ã¦ã—ã‚ˆã†ã¨ã™ã‚‹ã¨ã€57 ãŒ 56 ã«ãªã£ãŸã‚Šã™ã‚‹ã®ã§ã€å››æ¨äº”å…¥ã«ã™ã‚‹
